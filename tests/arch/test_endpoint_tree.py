@pytest.mark.arch
def test_grouping_by_typical_caller(fixture_endpoints: list[Endpoint]) -> None:
    by_path = {(e.method, e.path): e for e in fixture_endpoints}
    assert by_path[("GET", "/agents")].group == GROUP_OPERATOR
    assert by_path[("POST", "/capabilities:execute")].group == GROUP_AGENT
    assert by_path[("POST", "/credentials")].group == GROUP_ANY
    assert by_path[("GET", "/health")].group == GROUP_PUBLIC


@pytest.mark.arch
def test_explicit_public_is_public(fixture_endpoints: list[Endpoint]) -> None:
    """A public operation is never grouped as "any authenticated"."""
    by_path = {(e.method, e.path): e for e in fixture_endpoints}
    token = by_path[("POST", "/oauth/token")]
    assert token.public is True
    assert token.authenticated is False
    assert token.group == GROUP_PUBLIC


