@pytest.mark.arch
def test_identity_dependency_recognition_is_qualname_scoped() -> None:
    """Only the real get_current_identity closure is treated as the auth dep.

    A same-named free variable on an unrelated dependency factory must NOT be
    misclassified as authentication (this is a security classification path).
    """
    # The genuine identity dependency is recognised, with its scope/actor read off.
    real_dep = get_current_identity(
        required_permissions=["agents:read"], require_actor_type=ActorType.SERVICE_ACCOUNT
    ).dependency
    is_identity, perms, actor = _closure_values(real_dep)
    assert is_identity is True
    assert perms == ["agents:read"]
    assert actor is ActorType.SERVICE_ACCOUNT

    # An impostor closing over identically-named freevars is NOT the identity dep.
    def _make_impostor() -> object:
        required_permissions = ["org:admin"]
        require_actor_type = ActorType.USER

        def _other_dependency() -> None:
            _ = (required_permissions, require_actor_type)

        return _other_dependency

    assert _closure_values(_make_impostor()) == (False, None, None)


@pytest.mark.arch
def test_committed_reference_matches_generated() -> None:
    """docs/reference/endpoints.{md,json} must equal what the generator produces.

    Regenerate with ``make endpoints`` and commit the result.
    """
    endpoints = collect()

    expected_md = render_markdown(endpoints)
    actual_md = ENDPOINTS_MD.read_text(encoding="utf-8")
    assert actual_md == expected_md, (
        "docs/reference/endpoints.md is out of date with the code. "
        "Regenerate with `make endpoints` and commit the result."
    )

    expected_json = json.loads(render_json(endpoints))
    actual_json = json.loads(ENDPOINTS_JSON.read_text(encoding="utf-8"))
    assert actual_json == expected_json, (
        "docs/reference/endpoints.json is out of date with the code. "
        "Regenerate with `make endpoints` and commit the result."
    )


@pytest.mark.arch
def test_declared_broker_surface_matches_real_broker_app() -> None:
    """The declared broker surface must match the real broker app's routes.

    The live ``GET /reference/endpoints.json`` is mounted only on the control-plane
    app and cannot introspect the broker (it runs as a separate process), so the
    broker surface is declared in ``endpoint_reference`` and used when no broker app
    is passed. This builds the real broker app and asserts the declaration matches
    it exactly, so the declaration cannot silently drift from the broker.
    """
    introspected = _endpoints_from_app(_build_broker_app(), "broker")
    _annotate_broker(introspected)
    declared = _declared_broker_endpoints()

    def _sorted(eps: list[Endpoint]) -> list[dict[str, object]]:
        return sorted((asdict(e) for e in eps), key=lambda d: (d["path"], d["method"]))

    assert _sorted(introspected) == _sorted(declared), (
        "Declared broker surface (_declared_broker_endpoints) drifted from the real "
        "broker app. Update the declaration in endpoint_reference.py."
    )
    # Guard against a no-op (e.g. both empty) silently passing.
    assert {(e.path, e.method) for e in declared} >= {("/{upstream_url}", "POST")}


@pytest.mark.arch
def test_live_reference_matches_committed() -> None:
    """The live endpoint payload (broker=None) must equal the committed file.

    ``reference_router`` calls ``collect_endpoints(app)`` with no broker app, so this
    reproduces exactly what the live ``GET /reference/endpoints.json`` serves and
    asserts it equals the committed ``docs/reference/endpoints.json`` the docs SPA
    and CLI render — catching live-vs-committed drift (notably the broker surface).
    """
    live_payload = build_reference_payload(collect_endpoints(_build_control_app()))
    committed = json.loads(ENDPOINTS_JSON.read_text(encoding="utf-8"))
    committed.pop("_generated", None)
    assert live_payload == committed, (
        "Live GET /reference/endpoints.json (broker=None) differs from committed "
        "docs/reference/endpoints.json. The broker surface declaration or the "
        "committed file is stale; run `make endpoints` and reconcile."
    )
