@pytest.mark.arch
def test_classification_guard_rejects_undeclared_public() -> None:
    """A non-public-declared op rendered public must trip the fail-loud guard."""
    authed = [
        _ep("GET", f"/thing/{i}", actor_types=["user"], operation_id=f"thing{i}") for i in range(12)
    ]
    leaked = _ep(
        "POST",
        "/secret",
        authenticated=False,
        public=True,
        operation_id="secretOp",
        summary="Leaked",
    )
    with pytest.raises(RuntimeError, match="failed open"):
        assert_classification_is_sound([*authed, leaked], frozenset({"getHealth"}))


@pytest.mark.arch
def test_classification_guard_rejects_empty_recovery() -> None:
    """If almost nothing authenticates, introspection likely broke — fail loudly."""
    public_health = _ep(
        "GET",
        "/health",
        authenticated=False,
        public=True,
        operation_id="getHealth",
        summary="Health",
    )
    with pytest.raises(RuntimeError, match="scope introspection likely broke"):
        assert_classification_is_sound([public_health], frozenset({"getHealth"}))


@pytest.mark.arch
def test_broker_guard_allows_known_probes() -> None:
    """The broker's liveness/readiness probes may legitimately be public."""
    broker = [
        _ep("GET", "/health", authenticated=False, public=True, surface="broker"),
        _ep("GET", "/ready", authenticated=False, public=True, surface="broker"),
        _ep(
            "POST",
            "/{upstream_url}",
            required_scopes=["capabilities:execute"],
            typical_caller="agent",
            surface="broker",
        ),
    ]
    # No exception: only the known probes are public.
    assert_broker_classification_is_sound(broker)


