"""Drift-guard + unit tests for the endpoint + scope reference generator.

``docs/reference/endpoints.{md,json}`` are generated by ``tools/endpoint_tree``
(``make endpoints``). These tests assert the committed artifacts match what the
generator produces today, and that the join/grouping/render logic behaves on
fixture endpoints, so the reference can't silently drift from code.

The join itself lives in ``jentic_one.shared.web.endpoint_reference`` (shared with
the live ``GET /reference/endpoints.json`` endpoint); the renderers live in
``tools.endpoint_tree``.
"""

